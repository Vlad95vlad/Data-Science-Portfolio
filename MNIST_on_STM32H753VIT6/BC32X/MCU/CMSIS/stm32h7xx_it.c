/**
  * @brief This function handles System tick timer.
  */
/**
  * @brief  This function handles SysTick Handler.
  * @param  None
  * @retval None
  */
void SysTick_Handler(void)
{
   HAL_IncTick();

   if(xTaskGetSchedulerState() != taskSCHEDULER_NOT_STARTED)
   {
      xPortSysTickHandler();
   }
}

